package com.syntax.class02;

public class VariableValues {

	public static void main(String[] args) {
		
		//create variable= dataType, name and value, lastly reassign value
		
		short year=2022; 
		
		System.out.println(year);
		
		year= 2023;
				
	    System.out.println(year);
		
		//create variable first, assign value after, lastly reassign value

		int age;
		age=45;
		
		System.out.println(age);
		
		age=75;
		
		System.out.println(age);
		
		//create 3 int variables with different value, lastly reassign value
		
		int weight1, weight2, weight3;
		weight1= 100;
		weight2= 150;
		weight3= 200;
		
		System.out.println(weight1);
		System.out.println(weight2);
		System.out.println(weight3);
		
		weight1= 250;
		weight2= 300;
		weight3= 350;
		
		System.out.println(weight1);
		System.out.println(weight2);
		System.out.println(weight3);
	}

}
