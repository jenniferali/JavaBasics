package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		// Print numbers from 1 to 90
		
		for (int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
/* 
 * start point
 * end point
 * increment/decrement
 */
		
		// Print numbers from 60 to 10
		
		for (int j = 60; j>=10; j--) {
			System.out.print(j+" ");
		}
		
	}

}
