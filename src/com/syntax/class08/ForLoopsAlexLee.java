package com.syntax.class08;

public class ForLoopsAlexLee {

	public static void main(String[] args) {
		
		
		
		for(int i = 0; i < 5; i++) {
			System.out.println("I love puppers!");
		}
		
		System.out.println();
		
		for (int i = 0; i <= 15; i++) {
			System.out.print(i +" ");
		}
		System.out.println();
		
		int [] grades = {98, 100, 90, 93, 80};
		
		for (int i = 0; i < grades.length; i++) {
			System.out.print(grades[i] +" ");
		}
	}

}
