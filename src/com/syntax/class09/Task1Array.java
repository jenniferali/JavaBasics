package com.syntax.class09;

public class Task1Array {

	public static void main(String[] args) {
		
		char [] grade = new char [6];
		
		//Store Values:
		grade[0] = 'A';
		grade[1] = 'B';
		grade[2] = 'C';
		grade[3] = 'D';
		grade[4] = 'E';
		grade[5] = 'F';
		
		System.out.println(grade[1]);	
		
		System.out.println("------------------");
		
		char [] grade1 = {'A', 'B', 'C', 'D', 'E', 'F'};
		
		System.out.println(grade1[1]);
		
		
	}

}
